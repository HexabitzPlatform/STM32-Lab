uint16_t  adc_value= 0;
uint16_t voltage=0;
int8_t* str;
char buffer[16];
uint8_t MSG[35] = {'\0'};
uint8_t X = 0;

/* Private function prototypes -----------------------------------------------*/
void SystemClock_Config(void);
static void MX_GPIO_Init(void);
